user_progress = {}
user_answers = {}

def get_keyboard(options):
    kb = ReplyKeyboardMarkup(resize_keyboard=True)
    for opt in options:
        kb.add(KeyboardButton(f"{opt['emoji']} {opt['text']}"))
    return kb

async def ask_question(message, q_num):
    if q_num >= len(questions):
        answers = user_answers.get(message.from_user.id, [])
        counts = {}
        for ans in answers:
            counts[ans] = counts.get(ans, 0) + 1
        result = max(counts, key=counts.get) if counts else None
        titles = {
            "—Ç–µ—Ö–Ω–∞—Ä—å": "–¢–µ—Ö–Ω–∞—Ä—å",
            "—Ä—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª—å": "–†—É–∫–æ–≤–æ–¥–∏—Ç–µ–ª—å",
            "—Ç–≤–æ—Ä–µ—Ü": "–¢–≤–æ—Ä–µ—Ü",
            "—Ä–∞–±–æ—á–∏–π": "–†–∞–±–æ—á–∏–π"
        }
        if result:
            await message.answer(f"üìù –í–∞—à —Ä–µ–∑—É–ª—å—Ç–∞—Ç: {titles[result]}", reply_markup=types.ReplyKeyboardRemove())
        else:
            await message.answer("–ß—Ç–æ-—Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫...", reply_markup=types.ReplyKeyboardRemove())
        return

    q = questions[q_num]
    text = f"–í–æ–ø—Ä–æ—Å {q_num+1} –∏–∑ {len(questions)}:\n{q['text']}"
    kb = get_keyboard(q["options"])
    await message.answer(text, reply_markup=kb)
    user_progress[message.from_user.id] = q_num

@dp.message_handler(commands=['start'])
async def start_handler(message: types.Message):
    user_progress[message.from_user.id] = 0
    user_answers[message.from_user.id] = []
    await message.answer("–ü—Ä–∏–≤–µ—Ç! –≠—Ç–æ ¬´–ü–ª—ë–Ω–∫–∞ —Å—É–¥—å–±—ã¬ª\n–¢–µ—Å—Ç –∏–∑ 14 –≤–æ–ø—Ä–æ—Å–æ–≤ –ø–æ–∫–∞–∂–µ—Ç, –∫–µ–º –±—ã —Ç—ã –±—ã–ª(–∞) –≤ XX –≤–µ–∫–µ.")
    await asyncio.sleep(1.5)
    await ask_question(message, 0)

@dp.message_handler(lambda message: message.text and message.from_user.id in user_progress)
async def handle_answer(message: types.Message):
    q_num = user_progress[message.from_user.id]
    text = message.text.split(" ", 1)[1]  # —É–±–∏—Ä–∞–µ–º emoji –∏ –ø—Ä–æ–±–µ–ª
    user_answers[message.from_user.id].append(text)
    await ask_question(message, q_num + 1)

